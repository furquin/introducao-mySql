SELECT id FROM products ORDER BY id DESC  LIMIT 5;

-- SELECT => seleciona algo
-- id => coluna que será selecionada
-- FROM => de onde será selecionado
-- products => tabela que será usada para buscar as colunas
-- ORDER BY => ordena itens a partir de coluna
-- id => coluna que será usada para ordenar
-- DESC => indica que será ordenado de forma decrescente "por padrão ORDER BY e ASC"
-- LIMIT => limita o número de linhas para aparecer
